package application;

public class AssData {
	
	private String Ass;
	private String Type;
	private int Weight;
	
	public AssData(String Ass, String Type, int Weight) {
		this.Ass = Ass;
		this.Type = Type;
		this.Weight = Weight;
	}
	
	public String getAss() {
		return Ass;
	}
	
	public String getType() {
		return Type;
	}
	
	public int getWeight() {
		return Weight;
	}
	

}
